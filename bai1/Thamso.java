package bai1;

public class Thamso {

        public static int sum(int a, int b) {
            return a + b;
        }

        public static void main(String[] args) {
            int num1 = 5;
            int num2 = 10;
            int result = sum(num1, num2);
            System.out.println("Tổng của " + num1 + " và " + num2 + " là: " + result);
        }
    }
//    Trong ví dụ trên, ta có một phương thức `sum` với hai tham số kiểu int: a và b. Phương thức này trả về tổng của a và b. Trong phương thức `main`, ta định nghĩa hai biến num1 và num2 với giá trị lần lượt là 5 và 10. Sau đó, ta gọi phương thức sum và truyền giá trị của num1 và num2 vào phương thức. Giá trị trả về được gán cho biến result và sau đó in ra màn hình.
//
//        Khi thực hiện phương thức sum, giá trị của a được gán bằng giá trị của biến num1 và giá trị của b được gán bằng giá trị của biến num2. Sau đó, phương thức thực hiện phép tính tổng của a và b và trả về kết quả.
//
//        Ví dụ trên là một ví dụ đơn giản về phương thức có tham số trong Java. Trong thực tế, khi phát triển phần mềm, ta sẽ sử dụng các phương thức có nhiều tham số và các kiểu dữ liệu khác nhau cho các tham số đó.


